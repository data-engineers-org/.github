// Review via GitHub repo URL
app.post("/api/review/repo", async (req, res) => {
  const { repoUrl, branch } = req.body;
  if (!repoUrl) return res.status(400).json({ error: "repoUrl is required" });

  const tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), "pg-repo-"));

  try {
    // Parse repo name from URL
    const repoName = repoUrl.replace(/https?:\/\/github\.com\//, "").replace(/\.git$/, "").replace(/\/$/, "");

    // Clone the repository
    const cloneArgs = branch ? `--depth 1 --branch ${branch}` : "--depth 1";
    execSync(`git clone ${cloneArgs} ${repoUrl} ${tmpDir}/repo`, {
      stdio: "pipe",
      timeout: 60000,
    });

    // Find Python files
    const pyFiles = getPythonFiles(path.join(tmpDir, "repo"));
    if (pyFiles.length === 0) {
      return res.json({
        report: `# 📋 Policy Review Report\n\n> **⚠️ This is a READ-ONLY report. No files were modified.**\n\n## ✅ Overall Result: **PASS**\n\n> No Python files found in \`${repoName}\`. Nothing to review.`,
        filesScanned: 0,
      });
    }

    // Build prompt and run Copilot agent
    const prompt = buildPrompt(repoName, pyFiles);
    const report = await runCopilotReview(prompt);

    res.json({ report, filesScanned: pyFiles.length, repository: repoName });
  } catch (err) {
    console.error("Review error:", err);
    res.status(500).json({ error: err.message || "Review failed" });
  } finally {
    fs.rmSync(tmpDir, { recursive: true, force: true });
  }
});

// Review via ZIP upload
app.post("/api/review/upload", upload.single("archive"), async (req, res) => {
  if (!req.file) return res.status(400).json({ error: "No file uploaded" });

  const tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), "pg-zip-"));

  try {
    const projectName = req.body.projectName || path.parse(req.file.originalname).name;

    // Extract ZIP
    const zip = new AdmZip(req.file.path);
    zip.extractAllTo(tmpDir, true);

    // Find Python files
    const pyFiles = getPythonFiles(tmpDir);
    if (pyFiles.length === 0) {
      return res.json({
        report: `# 📋 Policy Review Report\n\n> **⚠️ This is a READ-ONLY report. No files were modified.**\n\n## ✅ Overall Result: **PASS**\n\n> No Python files found in uploaded archive. Nothing to review.`,
        filesScanned: 0,
      });
    }

    // Build prompt and run Copilot agent
    const prompt = buildPrompt(projectName, pyFiles);
    const report = await runCopilotReview(prompt);

    res.json({ report, filesScanned: pyFiles.length, projectName });
  } catch (err) {
    console.error("Upload review error:", err);
    res.status(500).json({ error: err.message || "Review failed" });
  } finally {
    fs.rmSync(tmpDir, { recursive: true, force: true });
    if (req.file) fs.rmSync(req.file.path, { force: true });
  }
});

app.listen(PORT, () => {
  console.log(`\n🚀 Policy Gate Web running at http://localhost:${PORT}\n`);
});
