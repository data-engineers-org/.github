/**
 * Extract Python files from a directory, returning their paths and contents.
 */
function getPythonFiles(dir) {
  const results = [];
  const SKIP = new Set([".git", "__pycache__", "venv", ".venv", "node_modules", "env", ".env"]);

  function walk(current, relBase) {
    for (const entry of fs.readdirSync(current, { withFileTypes: true })) {
      if (SKIP.has(entry.name)) continue;
      const fullPath = path.join(current, entry.name);
      const relPath = path.join(relBase, entry.name);
      if (entry.isDirectory()) {
        walk(fullPath, relPath);
      } else if (entry.name.endsWith(".py")) {
        try {
          const content = fs.readFileSync(fullPath, "utf-8");
          results.push({ path: relPath, content });
        } catch (_) { /* skip unreadable files */ }
      }
    }
  }

  walk(dir, "");
  return results;
}

/**
 * Build the full prompt with all Python file contents included.
 */
function buildPrompt(repoName, pyFiles) {
  let prompt = POLICY_AGENT_PROMPT;
  prompt += `\nRepository: ${repoName}\n`;
  prompt += `Total Python files: ${pyFiles.length}\n\n`;

  for (const file of pyFiles) {
    prompt += `--- FILE: ${file.path} ---\n`;
    prompt += file.content;
    prompt += `\n--- END FILE ---\n\n`;
  }

  prompt += `\nNow produce the complete Policy Review Report for this repository. Check every file against all 7 policies.`;
  return prompt;
}

/**
 * Run the Copilot SDK agent with the policy review prompt.
 */
async function runCopilotReview(prompt) {
  const client = new CopilotClient();
  await client.start();

  try {
    const session = await client.createSession({
      model: "gpt-4.1",
      instructions: "You are a read-only code policy auditor. You NEVER modify files. You ONLY produce reports.",
    });

    const response = await session.sendAndWait({ prompt });
    return response?.data?.content || "No response received from Copilot agent.";
  } finally {
    await client.stop();
  }
}

